			<p>
                <span class="emphasis">D. Sachs (2010).
                <a href="http://www.youtube.com/watch?v=C7JQ7Rpwn2k">
                Sensor Fusion on Android Devices: A Revolution in Motion Processing</a>. In <i>Google Tech Talk</i>.</span>
				
                Gyroscopes, accelerometers, and compasses are increasingly prevalent in mainstream consumer electronics. Applications of these sensors include user interface, augmented reality, gaming, image stabilization, and navigation. This talk will demonstrates how all three sensor types work separately and in conjunction on a modified Android handset running a modified sensor API, then explains how algorithms are used to enable a multitude of applications.
			</p>
            <p>
                <span class="emphasis">E. Miluzzo, N. Lane, K. Fodor, R. Peterson, H. Lu, M. Musolesi, S. Eisenman,
                X. Zheng and A. Campbell (2008).
                <a href="http://www.cs.dartmouth.edu/~sensorlab/pubs/cenceme_sensys08.pdf">
                Sensing Meets Mobile Social Networks: The Design, Implementation and
                Evaluation of the CenceMe Application</a>. In <i>The
                6th ACM Conference on Embedded Networked Sensor Systems</i>, 337-350.</span>

				CenceMe is an application that combines sensor enabled-mobile phones with social networks. The intention is to be able to automatically identify a user's actions using accelerometer, GPS, Bluetooth, and audio sensor data. The information is then broadcast through the userâ€™s various social media channels.
			</p>
            <p>
                <span class="emphasis">M. Weiser (1991).
                <a href="http://www.cim.mcgill.ca/~jer/courses/hci/ref/weiser_reprint.pdf">
                	The Computer for the 21st Century</a>,
                <i>Scientific American</i>, 94-10.</span>

				Weiser forecasts a future that has embraced ubiquitous computing. Ubiquitous computing refers to the complete integration of computers into everyday life. Weiser discusses the lifestyle changes that would occur in the workplace and at home. His predictions, made 20 years ago, have for the most part been realized, with only a few exceptions.
