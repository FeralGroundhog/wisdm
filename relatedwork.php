        <p class="title" data-section-title><a href="#">Biometrics</a></p>
        <div class="content" data-section-content>
          <p>
            	<span class="emphasis">Mauro Conti, Irina Zachia-Zlatea, and Bruno Crispo, 
                <a href="http://www.cs.vu.nl/~mconti/papers/C19.pdf">Mind How You Answer 
                Me! (Transparently Authenticating the User of a Smartphone when Answering 
                or Placing a Call)</a>. In <i>Proceedings of the Sixth ACM Symposium on 
                Information, Computer and Communications Security</i> (ACM SIGSAC ASIACCS 
                2011), Hong Kong, March 22-24, 2011.</span>
			</p>
            	
            <p>
                <span class="emphasis">J. Frank, S. Mannor, and D. Precup,
                <a href="http://www.cs.mcgill.ca/~jfrank8/pubs/aaai10-actrec.pdf"> Activity
                and Gait Recognition with Time-Delay Embeddings</a>. In <i>Proceedings of the
                24th AAAI Conference on Artificial Intelligence</i>, Atlanta, GA, 2010.</span>
                
                There is a related <a href="http://watch.discoverychannel.ca/#clip370449">clip</a>
                on the Discovery channel (the clip starts midway through the segment).
             </p>   
                
            <p>
                <span class="emphasis">R. Srinivasan, C. Chen, and D. Cook (2010). 
                <a href="http://eecs.wsu.edu/~cook/pubs/kdd10p3.pdf">Activity Recognition using
                Actigraph Sensor</a>. In <i>Proceedings of the 4th International
                workshop on Knowledge Discovery from Sensor Data</i>, 2010.</span>
			</p> 
                
            <p>
                <span class="emphasis">D. Gafurov and E. Snekkenes (2009).
                <a href="http://www.hindawi.com/journals/asp/2009/415817.html"> Gait
                Recognition Using Wearable Motion Recording Sensors</a>. In
                <i>EURASIP Journal on Advances in Signal Processing</i>, 2009:
                Article ID 415817.</span>
                
                Researchers analyzed data collected from motion recording sensors with
                tri-axial accelerometers placed on the foot, hip, pocket, and arm (in
                separate experiments). They examined the best performances of recognition
                methods based on the motion of these different body parts, as well as how
                robust gait-based authentication is under three attack scenarios and what
                attributes contribute to the uniqueness of human gait.
			</p> 
			    
            <p>
                <span class="emphasis">S. Zahid, M. Shazhad, S.A. Khayem, M. Farooq (2009). 
                <a href="http://www.genetic-programming.org/hc2009/5-Shahzad/Shahzad-Paper-RAID-2009.pdf">
		Keystroke-based User Identification on Smart Phones</a>. In
                <i>Proceeding RAID '09 Proceedings of the 12th International Symposium on 
		Recent Advances in Intrusion Detection</i>, Springer-Verlag, 2009.</span>
                
				Researchers analyze keystroke data of smart phone users and produce 
				a form of user identification. Six distinguishing keystroke
				features are used as the basis to identify the 25 smart phone users 
				who participated in the study. A fuzzy classifier was used to cluster 
				and classify the keystroke features. Particle Swarm Optimizer (PSO) 
				and Genetic Algorithm (GA) were used as front-end fuzzy classifier optimizer 
				and as a back-end optimizer respectively. A PIN verification mode using 
				keystroke dynamics was also provided in order to guarantee information 
				security on the smart phones.			
			</p>
                
            <p>
                <span class="emphasis">D. Gafurov (2007).
                <a href="http://www.iu.hio.no/nik07/bidrag/Gafurov.pdf">
                A Survey of Biometric Gait Recognition: Approaches, Security, and
                Challenges</a>. In <i>Annual Norwegian Computer Science Conference</i>,
                November 19-21, 2007.</span>
                
                Gafurov presents an overview of recent biometric
                research focusing on gait recognition. He first describes basic evaluation
                metrics in  biometrics, including false accept rates, false reject rates,
                equal error rates, DET curves, and CMC curves. He identifies three types of
                gait recognition--machine vision based, floor sensor based, and wearable
                sensor based--and describes various challenges that the wearable sensor
                based-approach encounters.
			</p>  
                
            <p>
                <span class="emphasis">D. Gafurov, K. Helkala and T. Sondrol (2006). Biometric Gait Authentication 
                Using Accelerometer Sensor. In <i>Journal of Computers</i>, 1(7):51-59.</span>
                
                This paper examines how gait patterns can be used as an unobtrusive means
                to authenticate the identity of the user. Gait patterns were recorded by
                attaching an accelerometer to the right lower leg of the user. Histogram
                similarity and cycle length were used as part of the authentication
                procedure, which was tested on a population of 21 participants.
			</p>
                
            <p>
                <span class="emphasis">J. Mantyjarvi, M. Lindholdm,et. al. (2005). Identifying Users of Portable
                Devices from Gait Pattern with Accelerometers. In <i>Proceedings of IEEE
                International Conference on Acoustics, Speech, and Signal Processing 
                (ICASSP '05)</i>.</span>
                
                The goal of this work was to identify users using data
                from accelerometers in portable devices that users normally carry. Data was
                collected from 36 subjects who walked at fast, normal, and slow walking
                speeds on two separate days. The accelerometer device was placed on their
                belts, at the middle of their waistline in back.  Correlation, frequency
                domain, and data distribution statistics were used to identify users.             
                
            </p>
