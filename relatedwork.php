        <p class="title" data-section-title><a href="#">Geo-spatial Data Mining (of GPS)</a></p>
        <div class="content" data-section-content>
          <p>
                <span class="emphasis">G. Agamennoni, J. Nieto, E. Nebot (2008). <a href="http://www.araa.asn.au/acra/acra2008/papers/pap115s1.pdf">
                Mining GPS Data for Extracting Significant Place</a>. In <i>Proceedings of the 2008 IEEE International 
                Conference on Robotics and Automation</i>, 2008.</span>
                
                Researchers present a fast and robust algorithm for extracting significant places 
                from a set of raw GPS data points. The research took place in Australia at a mining 
                field for the purpose of improving mine safety by exploiting the large amounts of raw 
                GPS data points. Using the hauling vehicles, data was gathered and collected using a 
                scoring algorithm to determine the places of significance.
			</p>
                
            <p>
                <span class="emphasis">D. Luper, D. Cameron, J.A. Miller, H. Arabnia (2007). Spatial and 
                Temporal Target Association through Semantic Analysis and GPS Data Mining. In <i>the 2007 
                World Congress in Computer Science, Computer Engineering, &amp; Applied Computing (IKE '07)</i>, 
                Las Vegas, USA, June 25-28 2007.</span>
                
                Researchers analyzed temporal and spatial interactions in an Association Network that 
                integrated GPS, RDF Metadata, and Data Mining. The experiment generated random data for 133 
                "targets," 33 of which were "terrorists" and the rest were control units. Data was recorded for 
                60 days at 10 second intervals and kept the generation within Athens, GA. They implemented Data 
                Mining tools such as Binning and Association Rules to find interesting places and "targets" that 
                were associated with other "targets." The probability of someone being at a meeting if the "target" 
                was at a meeting and the likelihood of someone visiting a place if the "target" visited the place 
                were found and using these statistics and multiplying them by their semantic scores, spatial being 
                in the same area as a target and temporal being people who met with the target, two final results 
                were produced.
			</p>
                
            <p>
                <span class="emphasis">S. Khetarpaul, R. Chauhan, SK Gupta, LV Subramaniam, U. Nambiar (2011). 
                Mining GPS Data to Determine Interesting Locations. In <i>Proceedings of the 8th International 
                Workshop on Information Integration on the Web</i>, New York, NY, USA 2011.</span>
                
                The researchers analyzed aggregate GPS info of 62 users over 2 years to mine the list of interesting 
                location rank, which was determined by visits per user. They used relational Algebra Operations and 
                Statistical formulas to determine these "interesting locations." The main topic dealt with was "Stay 
                Point Determination," wherein an individual spent a significant amount of time in a location, either 
                indoors â€“ where they would enter, lose signal, and exit â€“ or a general outdoor location, both determined 
                by a threshold.
			</p>
                
